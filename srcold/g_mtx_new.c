#include "../../inc/fdf.h"

t_matrix		g_new_mtx(t_vector x, t_vector y, t_vector z)
{
	t_vector	w;
	t_matrix	out;

	out.mx = x;
	out.my = y;
	out.mz = z;
	out.mw = w;
	return (out);
}

