#include "../inc/fdf.h"

t_vct4			*shape_cube()
{
	t_vct4		*out;

	out = (t_vct4 *)v_alloc(sizeof(t_vct4) * 8);
	out[0] = g_vct4(0, 0, 0, 1);
	out[1] = g_vct4(0, 1, 0, 1);
	out[2] = g_vct4(1, 0, 0, 1);
	out[3] = g_vct4(1, 1, 0, 1);
	out[4] = g_vct4(0, 0, 1, 1);
	out[5] = g_vct4(0, 1, 1, 1);
	out[6] = g_vct4(1, 0, 1, 1);
	out[7] = g_vct4(1, 1, 1, 1);
	return (out);
}
